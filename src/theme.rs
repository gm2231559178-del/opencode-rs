use ratatui::style::Color;

pub struct Theme {
    pub name: &'static str,
    pub primary: Color,
    pub secondary: Color,
    pub accent: Color,
    pub success: Color,
    pub warning: Color,
    pub error: Color,
    pub text: Color,
    pub text_muted: Color,
    pub text_dim: Color,
    pub bg: Color,
    pub background_panel: Color,
    pub background_element: Color,
    pub border: Color,
    pub border_active: Color,
    pub dim: Color,
    pub user_msg: Color,
    pub assistant_msg: Color,
    pub tool_call: Color,
    pub tool_result: Color,
    pub diff_add: Color,
    pub diff_del: Color,
    pub diff_hunk: Color,
    pub syntax_keyword: Color,
    pub syntax_string: Color,
    pub syntax_number: Color,
    pub syntax_comment: Color,
    pub syntax_builtin: Color,
}

impl Theme {
    pub fn by_name(name: &str) -> &'static Theme {
        match name {
            "tokyonight" => &TOKYONIGHT,
            "catppuccin" => &CATPPUCCIN,
            "gruvbox" => &GRUVBOX,
            "dracula" => &DRACULA,
            "nord" => &NORD,
            "onedark" => &ONEDARK,
            _ => &DEFAULT,
        }
    }
}

