use ratatui::style::Color;

pub struct Theme {
    pub name: &'static str,
    pub primary: Color,
    pub secondary: Color,
    pub accent: Color,
    pub success: Color,
    pub warning: Color,
    pub error: Color,
    pub dim: Color,
    pub bg: Color,
    pub text: Color,
    pub text_dim: Color,
    pub border: Color,
    pub user_msg: Color,
    pub assistant_msg: Color,
    pub tool_call: Color,
    pub tool_result: Color,
}

impl Theme {
    pub fn by_name(name: &str) -> &'static Theme {
        match name {
            "tokyonight" => &TOKYONIGHT,
            "catppuccin" => &CATPPUCCIN,
            "gruvbox" => &GRUVBOX,
            "dracula" => &DRACULA,
            "nord" => &NORD,
            "onedark" => &ONEDARK,
            _ => &DEFAULT,
        }
    }
}

